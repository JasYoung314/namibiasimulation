"""
A function to compute the sum of two values
"""

def sum_of_two_values(a,b):
    """
    Function to compute the sum of two values 

    Arguments: a - The first value to be used in the sum
               b - The second value to be used in the sum

    TESTS::

        >>> a,b = 2,3
        >>> sum_of_two_values(a,b)
        5
    """

    return a + b
